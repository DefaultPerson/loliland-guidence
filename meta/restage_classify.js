export const meta = {
  name: 'tm-restage-classify',
  description: 'Classify every step: keep/drop (author-specific), assign to one of 5 quest-stages, order hint, scrub personal-run content',
  phases: [{ title: 'Classify', detail: 'one opus agent per section -> research/restage/<key>.classify.json', model: 'opus' }],
}

const SECTIONS = ['start','tinkers','processing','thaumcraft','botania','bloodmagic','bees',
  'ae2','energistics','mekanism','dimensions','draconic','dragons','relics_elements','avaritia']

const SUMMARY = {
  type: 'object', additionalProperties: false,
  properties: { key:{type:'string'}, wrote:{type:'boolean'}, n_steps:{type:'integer'},
    n_drop:{type:'integer'}, n_scrubbed:{type:'integer'}, stages:{type:'object',additionalProperties:{type:'integer'}},
    note:{type:'string'} },
  required: ['key','wrote','n_steps','n_drop','n_scrubbed','stages','note'],
}

const STAGES = `ЭТАПЫ (целевая структура гайда — 5 линейных этапов-квестов вместо мод-разделов):
1 «Старт и обустройство» — личный реалм, киты, первые инструменты (TiC-молот/орихалк), еда, первая шахта по трём мирам, книга квестов, базовая экономика. Конец: есть база, молот, наносет в планах.
2 «Ранняя техника и первая автоматизация» — IC2-фундамент (энергия, базовые машины), первая ME-система AE2 и первый автокрафт, схема руды 1:6, наносет/квант, кинетика/ветряки/панели. Конец: работающая ME-сеть + стабильная энергия + удвоение руды.
3 «Развитие: параллельные ветки техники и магии» — Thaumcraft (исследования→инфузия), Botania (мана→Мультитеплица→Альфхейм), Blood Magic (алтарь T1-T6, реагенты), измерения/данжи/боссы среднего уровня, глубокая AE2-автоматизация (кодировщики loli_technologies), LoliEnergistics молекулярные тиры, удвоители руды/удача. Вдвоём ветки делятся. Конец: бесконечная мана, промышленная кровь, автоматизированная эссенция, миллионные EU.
4 «Эндгейм-сборка» — Draconic (ядро, пробуждение, синтез), драконы LoliDragonMight (души, реактор Хаоса, спавнеры), пчёлы (Gendustry после Драконьего слияния), реликвии/Элементы, солнечные панели высоких тиров, нейтрониевый конвейер, звёзды/микрозвёзды. Конец: миллиарды энергии, поток нейтрония, драконий контент закрыт.
5 «Финал: Дорога к Бесконечному» — Катализатор/Слиток Бесконечности, творческие предметы, Infinity-снаряжение, демонические сеты, THE TRUE END.`

function prompt(key) {
  return `Ты — редактор гайда «TechnoMagic RPG» (LoliLand). Мы ПЕРЕСТРАИВАЕМ гайд: вместо 15 мод-разделов будет 5 линейных этапов-квестов. Твоя задача — разметить КАЖДЫЙ шаг раздела «${key}»: куда он идёт, в каком порядке, и вычистить из него «личное прохождение автора видео».

Прочитай Read:
1. data/sections/${key}.json — твой раздел (шаги 0..N-1; у шагов есть поля detail/items/serverNote/coop/opt/kit/skip/trap/trick/tipv/optional).
2. research/youtube/sections/${key}.beats.json — хронология видео по этой теме (pct = % 16-часового прохождения; ориентир реального порядка). Файл может отсутствовать/быть большим — тогда пропусти или читай кусками.
3. research/youtube/progression/timing.json — когда каждая тема реально шла в видео.

${STAGES}

Запиши Write в research/restage/${key}.classify.json СТРОГО JSON-массив, по одному объекту на КАЖДЫЙ шаг (индексы 0..N-1, ни один не пропусти):
{
 "idx": <int>,
 "action": "keep" | "drop" | "fold",
 "stage": <1-5>,            // для drop тоже укажи, куда бы шёл
 "order": <число 0-100>,    // позиция ВНУТРИ этапа по реальной прогрессии (0=самое начало этапа, 100=конец). Ориентируйся на pct видео, крафт-зависимости и здравый смысл
 "fold_into": "<section#idx или '' >",  // для action=fold: с каким шагом слить (его суть — дубль другого шага, укажи какого; из ЛЮБОГО раздела)
 "scrub": { }               // ТОЛЬКО изменённые поля с вычищенным текстом (см. правила чистки); пустой объект если чистка не нужна
 ,"reason": "<кратко>"
}

ПРАВИЛА ЧИСТКИ («личное прохождение автора» — подумай критически):
- DROP целиком: шаги-«дневники» без переносимой пользы — логи конкретных дропов из кейсов/аирдропов («выпало полкирки искрящей материи», «квантовая коробка дала розовое золото»), «по просьбам зрителей дозакрыл квесты», перестройки базы автора, чистая нарративная связка. Но: если внутри такого шага есть СИСТЕМНАЯ механика (шанс кейсов повышен в выходные; аирдропы стоит собирать; за квест X дают Y) — не дропай, а SCRUB: перепиши detail, оставив механику и выкинув личный лут.
- SCRUB внутри keep-шагов: убери из detail/notes фразы вида «автору повезло», «у автора к этому моменту было N», конкретные RNG-дропы; «у автора» → обезличь («к этому этапу обычно...»). Цены с трейдов и числа энергии ОСТАВЛЯЙ (это ориентиры), но формулируй как ориентир. Заметки skip/trap/trick/tipv — ЦЕННОСТЬ, сохраняй (это переносимые уроки), чисти только личные RNG-детали внутри них.
- FOLD: если шаг по сути дублирует шаг другого раздела (одна и та же постройка/механика описана дважды) — пометь fold с указанием цели; содержимое сольют при сборке.
- Всё про вайп 18.07.2026 (пометки «ВАЙП…») — сохраняй как есть.
- Кооп-поле сохраняй.

Верни (schema): key, wrote, n_steps (=N, все размечены), n_drop, n_scrubbed, stages (сколько шагов в какой этап: {"1":..,"5":..}), note (1-2 фразы).`
}

log(`Classify: ${SECTIONS.length} sections (opus)`)
const out = await parallel(SECTIONS.map(key => () =>
  agent(prompt(key), { label: `cls:${key}`, phase: 'Classify', schema: SUMMARY, model: 'opus', effort: 'high' })
))
const ok = out.filter(Boolean)
log(`classified ${ok.length}/15; drops=${ok.reduce((a,r)=>a+r.n_drop,0)}`)
return { results: ok }
